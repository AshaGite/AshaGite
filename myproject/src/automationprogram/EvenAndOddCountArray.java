package automationprogram;
public  class EvenAndOddCountArray  {
public static void main(String[] args) {
	int a[]= {1,4,6,8,9,70};
	int evenCount=0;
	int oddCount=0;
	for(int i=0;i<a.length;i++) {
	if(a[i]%2==0) {
		evenCount=evenCount+1;
	}
	else {
		oddCount=oddCount+1;
	}
	
	}
	System.out.println(evenCount);
	System.out.println(oddCount);
	
}
	}

		
		
		
	


		
		
		
		
		
		

			
		

	
		






	

	

		

	
	

	







	

	










